class MultiscaleDiscriminator(BaseNetwork):
    @staticmethod
    def modify_commandline_options(parser, is_train):
        parser.add_argument('--netD_subarch', type=str, default='n_layer',
                            help='architecture of each discriminator')
        parser.add_argument('--num_D', type=int, default=2,
                            help='number of discriminators to be used in multiscale')
        opt, _ = parser.parse_known_args()

        # define properties of each discriminator of the multiscale discriminator
        subnetD = util.find_class_in_module(opt.netD_subarch + 'discriminator',
                                            'models.networks.discriminator')
        subnetD.modify_commandline_options(parser, is_train)

        return parser

    def __init__(self, opt):
        super().__init__()
        self.opt = opt

        for i in range(opt.num_D):
            subnetD = self.create_single_discriminator(opt)
            self.add_module('discriminator_%d' % i, subnetD)

    def create_single_discriminator(self, opt):
        subarch = opt.netD_subarch
        if subarch == 'n_layer':
            netD = NLayerDiscriminator(opt)
        else:
            raise ValueError('unrecognized discriminator subarchitecture %s' % subarch)
        return netD

    def downsample(self, input):
        return F.avg_pool2d(input, kernel_size=3,
                            stride=2, padding=[1, 1],
                            count_include_pad=False)

    # Returns list of lists of discriminator outputs.
    # The final result is of size opt.num_D x opt.n_layers_D
    def forward(self, input):
        result = []
        get_intermediate_features = not self.opt.no_ganFeat_loss
        for name, D in self.named_children():
            out = D(input)

            # if opt.no_ganFeat_loss, D outputs a single feature map. Else, D outputs a list of feature map.
            # This conditional is just to unify the return type of the discriminator
            if not get_intermediate_features:
                out = [out]

            result.append(out)
            input = self.downsample(input)

        return result


# Defines the PatchGAN discriminator with the specified arguments.
class NLayerDiscriminator(BaseNetwork):
    @staticmethod
    def modify_commandline_options(parser, is_train):
        parser.add_argument('--n_layers_D', type=int, default=4,
                            help='# layers in each discriminator')
        return parser

    def __init__(self, opt):
        super().__init__()
        self.opt = opt

        kw = 4
        padw = int(np.ceil((kw - 1.0) / 2))
        nf = opt.ndf
        # input_nc = self.compute_D_input_nc(opt)
        # An original SPADE discriminator calculates input_channel # with label_nc, wheter using instance, ...
        # We just use LAB(3) for input. Therefore the number of input channel should be 3 (which was 4 for LAB(3)+L(1) input)
        # TODO: modify it with opt
        input_nc = 3

        norm_layer = get_nonspade_norm_layer(opt, opt.norm_D)
        sequence = [[nn.Conv2d(input_nc, nf, kernel_size=kw, stride=2, padding=padw),
                     nn.LeakyReLU(0.2, False)]]

        for n in range(1, opt.n_layers_D):
            nf_prev = nf
            nf = min(nf * 2, 512)
            stride = 1 if n == opt.n_layers_D - 1 else 2
            sequence += [[norm_layer(nn.Conv2d(nf_prev, nf, kernel_size=kw,
                                               stride=stride, padding=padw)),
                          nn.LeakyReLU(0.2, False)
                          ]]

        sequence += [[nn.Conv2d(nf, 1, kernel_size=kw, stride=1, padding=padw)]]

        # We divide the layers into groups to extract intermediate layer outputs
        for n in range(len(sequence)):
            self.add_module('model' + str(n), nn.Sequential(*sequence[n]))

    def compute_D_input_nc(self, opt):
        input_nc = opt.label_nc + opt.output_nc
        if opt.contain_dontcare_label:
            input_nc += 1
        if not opt.no_instance:
            input_nc += 1
        return input_nc

    def forward(self, input):
        results = [input]
        for submodel in self.children():
            intermediate_output = submodel(results[-1])
            results.append(intermediate_output)

        get_intermediate_features = not self.opt.no_ganFeat_loss
        if get_intermediate_features:
            return results[1:]
        else:
            return results[-1]

#source: https://github.com/heykeetae/Self-Attention-GAN/blob/master/sagan_models.py
class Self_Attn(BaseNetwork):
    """ Self attention Layer"""

    def __init__(self, in_dim):
        super(Self_Attn, self).__init__()
        self.chanel_in = in_dim

        self.query_conv = nn.Conv2d(in_channels=in_dim, out_channels=in_dim // 8, kernel_size=1)
        self.key_conv = nn.Conv2d(in_channels=in_dim, out_channels=in_dim // 8, kernel_size=1)
        self.value_conv = nn.Conv2d(in_channels=in_dim, out_channels=in_dim, kernel_size=1)
        self.gamma = nn.Parameter(torch.zeros(1))

        self.softmax = nn.Softmax(dim=-1)  #

    def forward(self, x):
        """
            inputs :
                x : input feature maps( B X C X W X H)
            returns :
                out : self attention value + input feature
                attention: B X N X N (N is Width*Height)
        """
        m_batchsize, C, width, height = x.size()
        proj_query = self.query_conv(x).view(m_batchsize, -1, width * height).permute(0, 2, 1)  # B X CX(N)
        proj_key = self.key_conv(x).view(m_batchsize, -1, width * height)  # B X C x (*W*H)
        energy = torch.bmm(proj_query, proj_key)  # transpose check
        attention = self.softmax(energy)  # BX (N) X (N)
        proj_value = self.value_conv(x).view(m_batchsize, -1, width * height)  # B X C X N

        out = torch.bmm(proj_value, attention.permute(0, 2, 1))
        out = out.view(m_batchsize, C, width, height)

        out = self.gamma * out + x
        return out, attention

class SAGanDiscriminator(nn.Module):
    def __init__(self, in_height, in_width, inner_nc=64):

        self.norm = nn.BatchNorm2d

        self.conv1 = nn.Sequential(
            spectral_norm(nn.Conv2d(3, inner_nc, stride=2, kernel_size=4, padding=1)),
            self.norm(inner_nc)
        )

        self.self_attention = Self_Attn(inner_nc)
        self.convs = nn.Sequential(
            nn.Conv2d(inner_nc, inner_nc, stride=2, kernel_size=4, padding=1),
            spectral_norm(),
            nn.LeakyReLU(0.2, True),

            nn.Conv2d(inner_nc, inner_nc*2, stride = 2, kernel_size = 4, padding=1),
            spectral_norm(),
            nn.LeakyReLU(0.2, True),

            nn.Conv2d(inner_nc, inner_nc*4, stride = 2, kernel_size = 4, padding=1),
            spectral_norm(),
            nn.LeakyReLU(0.2, True),

            nn.Conv2d(inner_nc, inner_nc*8, stride = 2, kernel_size = 4, padding=1),
            spectral_norm(),
            nn.LeakyReLU(0.2, True),

            nn.Conv2d(inner_nc, inner_nc*16, stride = 2, kernel_size = 4, padding=1),
            spectral_norm(),
            nn.LeakyReLU(0.2, True),

            nn.Conv2d(inner_nc*16, 1, kernel_size=3)
        )
        in_height = in_height / (2 ** 6) - 2
        in_width = in_width / (2 ** 6) - 2

        kernel_size = (in_height, in_width)
        # global average pool
        self.pool = nn.AvgPool2d()
